function letterGrade(percentGrade) {
    if (percentGrade >= 80) {
        return "A"
    } else if (percentGrade >= 70) {
        return "B"
    }  else if (percentGrade >= 60) {
        return "C"
    }  else if (percentGrade >= 50) {
        return "D"
    }  else if (percentGrade >= 0) {
        return "F"
    } 
}
